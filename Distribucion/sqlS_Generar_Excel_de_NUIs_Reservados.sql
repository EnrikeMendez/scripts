FROM    WEB_LTL WEL
    INNER JOIN WEB_TRACKING_STAGE WTS ON WEL.WELCLAVE = WTS.NUI
    INNER JOIN WEB_LOTS WL ON WTS.NUMERO_LOTE = WL.LOTE
WHERE 1=1
